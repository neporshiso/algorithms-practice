def isValidSudoku(board):
    # this was used in a previous submission. Modified for this example.
    def duplicate_detection(list):
        tracker = {}
        list = [item for item in list if item != "."]
        for i in list:
            if i not in tracker:
                tracker[i] = 1
            else:
                return True

    # checks for duplicates across a row
    for row in board:
        row_detection = duplicate_detection(row)
        if row_detection == True:
            return False

    # uses list comprehension to isolate columns
    for i in range(len(board)):
        col = [row[i] for row in board]
        col_detection = duplicate_detection(col)
        if col_detection == True:
            return False

    return True


